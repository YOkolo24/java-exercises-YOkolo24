package com.cbfacademy;

public class ArithmeticExercises {
    double AddDouble(double operand1, double operand2) {
        return (operand1 + operand2);
    }

    double SubtractDouble(double operand1, double operand2) {
        return (operand1 - operand2);
    }

    double MultiplyDouble(double operand1, double operand2) {
        return (operand1*operand2);
    }

    double DivideDouble(double operand1, double operand2) {
        return (operand1 / operand2);
    }
}
